package itfmod.world;

public class TopBelowMinimunException extends Exception
{
	public TopBelowMinimunException()
	{
		super();
	}
	
	public TopBelowMinimunException(String message)
	{
		super(message);
	}
	
	public TopBelowMinimunException(Throwable cause)
	{
		super(cause);
	}
	
	public TopBelowMinimunException(String message, Throwable cause)
	{
		super(message, cause);
	}
}
